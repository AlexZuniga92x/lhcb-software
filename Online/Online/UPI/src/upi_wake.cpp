#include "WT/wt_facilities.h"
#include "WT/wtdef.h"
#include "UPI/upidef.h"

//---------------------------------------------------------------------------
static int Count = 0;
static int Fac = 0;
//---------------------------------------------------------------------------


//---------------------------------------------------------------------------
void upic_wakeup () {
  if (Fac)  {
    Count++;
    wtc_insert (Fac);
  }
}


//---------------------------------------------------------------------------
void upic_set_wakeup ()   {
  Count = 0;
  Fac = WT_FACILITY_SCR;
}

//---------------------------------------------------------------------------
int upic_test_wakeup () {
  int i = Count;
  Count = 0;
  return (i);
}

//---------------------------------------------------------------------------
void upic_reset_wakeup ()   {
  Fac = 0;
}
