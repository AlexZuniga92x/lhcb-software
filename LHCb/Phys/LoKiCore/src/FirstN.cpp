LoKi::Sort::Sort
( const int  N          , // ALL 
  const bool ascending  ) 
  : m_N         ( N         ) 
  , m_ascending ( ascending ) 
{}
// ============================================================================
// virtual destructor 
// ============================================================================
LoKi::Sort::~Sort (){}
// ============================================================================
// printout 
// ============================================================================
std::ostream& 
LoKi::Sort::fillStream ( std::ostream& s ) const
{ 
  s << " Sort(" ;
  if ( !m_ascending || 0 <= m_N ) { s << "," << m_N     ; }
  if ( !m_ascending || 0 <= m_N ) { s << "," << "False" ; }
  return s << ") " ;
}  
