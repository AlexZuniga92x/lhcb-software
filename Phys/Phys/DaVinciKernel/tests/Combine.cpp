StatusCode Combine::combine ( LHCb::Particle& p , LHCb::Vertex& v    ) 
{
  // some fake action 
  v.setPosition       ( p.referencePoint() ) ;
  p.setReferencePoint ( v.position      () ) ;
  //
  p.setEndVertex ( &v ) ;
  p.setParticleID ( LHCb::ParticleID( v.outgoingParticles().size() ) );
  //
  return StatusCode::SUCCESS ;
}
