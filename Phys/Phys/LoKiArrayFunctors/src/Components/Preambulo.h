#include <string>
#include <vector>
// ============================================================================
namespace 
{
  // ==========================================================================
  /// construct the preambulo string 
  inline std::string _preambulo ( const std::vector<std::string>& lines )
  {
    std::string result ;
    for ( std::vector<std::string>::const_iterator iline = 
            lines.begin() ; lines.end() != iline ; ++iline ) 
    {
      if ( lines.begin() != iline ) { result += "\n" ; }
      result += (*iline) ;
    }
    return result ;
  }
  // ==========================================================================
} //                                                 end of anonymous namespace 
